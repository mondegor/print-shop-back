CREATE TABLE printshop_auth.users_activity_stat (
    user_id uuid NOT NULL,
    realm_id int4 NOT NULL,
    last_login_ip inet NOT NULL, -- remote addr (берётся из RemoteAddr)
    last_logged_at  timestamp with time zone NOT NULL,
    last_visited_at timestamp with time zone NOT NULL,
    CONSTRAINT pk_users_activity_stat PRIMARY KEY (user_id, realm_id),
    CONSTRAINT fk_users_activity_stat FOREIGN KEY (user_id, realm_id)
        REFERENCES printshop_auth.users_realms(user_id, realm_id) ON DELETE CASCADE
);

-- --------------------------------------------------------------------------------------------------

CREATE TABLE printshop_auth.users_activity_log (
    record_id int8 NOT NULL GENERATED BY DEFAULT AS IDENTITY CONSTRAINT pk_users_activity_log PRIMARY KEY,
    user_id uuid NOT NULL,
    realm_id int4 NOT NULL, -- 0 = realm неизвестен (реестр realm'ов разошёлся с провайдерами)
    user_ip inet NOT NULL, -- remote addr (берётся из RemoteAddr)
    user_proxy_ip inet NULL, -- proxy addr (для информации)
    user_agent  character varying(512) NOT NULL,
    request_path character varying(256) NOT NULL,
    request_status int4 NOT NULL, -- 200, 403
    visited_at timestamp with time zone NOT NULL
);

CREATE INDEX ix_users_activity_log_visited_at ON printshop_auth.users_activity_log (visited_at);

