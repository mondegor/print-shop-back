CREATE TABLE printshop_auth.secure_operations_log (
    record_id int8 NOT NULL GENERATED BY DEFAULT AS IDENTITY CONSTRAINT pk_secure_operations_log PRIMARY KEY,
    visitor_id uuid NOT NULL, -- visitor_id or user_id
    operation_name character varying(64) NOT NULL,
    confirm_method int2 NOT NULL, -- 0=UNSPECIFIED, 1=EMAIL, 2=PHONE, 3=PASSWORD, 4=TOTP
    log_status int2 NOT NULL, -- 1=OPENED, 2=RESENT_CODE, 3=CONFIRM_SUCCESS, 4=CONFIRM_FAILED, 5=CONFIRMED, 6=REVOKED, 7=APPLIED, 8=BLOCKED, 9=SESSION_OPENED
    reason int2 NOT NULL DEFAULT 0, -- 0=UNSPECIFIED, 1=WRONG_CODE, 2=ATTEMPTS_EXHAUSTED, 3=THROTTLED, 4=TOKEN_REUSE, 5=ACCESS_FORBIDDEN, 6=TOTP_REPLAY, 7=EXPIRED, 8=NOT_CONFIRMED, 9=LOGIN_NOT_EXISTS, 10=SESSION_LIMIT
    client_ip inet NOT NULL, -- remote addr (берётся из RemoteAddr)
    client_proxy_ip inet NULL, -- proxy addr (для информации)
    created_at timestamp with time zone NOT NULL DEFAULT NOW()
);

CREATE INDEX ix_secure_operations_log_created_at ON printshop_auth.secure_operations_log (created_at);
CREATE INDEX ix_secure_operations_log_visitor_id_created_at ON printshop_auth.secure_operations_log (visitor_id, created_at);
CREATE INDEX ix_secure_operations_log_client_ip_created_at ON printshop_auth.secure_operations_log (client_ip, created_at);

