CREATE TABLE printshop_auth.secure_operations (
    operation_token character varying(128) NOT NULL CONSTRAINT pk_secure_operations PRIMARY KEY,
    operation_name character varying(64) NOT NULL,
    user_id uuid NULL, -- TODO: возможно, заменить на visitor_id
    confirm_actions jsonb NULL,
    remaining_attempts int2 NOT NULL CHECK(remaining_attempts >= 0), -- кол-во оставшихся попыток подтверждения текущего экшена операции
    remaining_resends int2 NOT NULL CHECK(remaining_resends >= 0), -- кол-во оставшихся попыток повторной отправки кода подтверждения
    resends_at timestamp with time zone NOT NULL, -- время, начиная с которого можно сделать повторную отправку кода подтверждения
    operation_payload bytea NULL, -- realm, visitorId, newEmail, ...
    operation_status int2 NOT NULL, -- 1=OPENED, 2=CONFIRMED, 3=UPDATING
    created_at timestamp with time zone NOT NULL DEFAULT NOW(),
    expires_at timestamp with time zone NOT NULL
);

CREATE INDEX ix_secure_operations_expires_at ON printshop_auth.secure_operations (expires_at);

