CREATE TABLE printshop_auth.users (
    user_id uuid NOT NULL CONSTRAINT pk_users PRIMARY KEY,
    user_email character varying(64) NOT NULL,
    user_phone int8 DEFAULT NULL,
    lang_code character varying(5) NOT NULL,
    user_status int2 NOT NULL, -- 1=DRAFT, 2=ENABLED, 3=DISABLED, 4=BLOCKED
    created_at timestamp with time zone NOT NULL DEFAULT NOW(),
    updated_at timestamp with time zone NOT NULL DEFAULT NOW(),
    deleted_at timestamp with time zone NULL
);

CREATE UNIQUE INDEX uk_users_user_email ON printshop_auth.users (user_email) WHERE deleted_at IS NULL;
CREATE UNIQUE INDEX uk_users_user_phone ON printshop_auth.users (user_phone) WHERE deleted_at IS NULL;

