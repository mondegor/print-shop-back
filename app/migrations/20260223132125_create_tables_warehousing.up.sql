CREATE TABLE warehousing.container_stocks (
    stock_id int8 NOT NULL GENERATED BY DEFAULT AS IDENTITY CONSTRAINT pk_container_stocks PRIMARY KEY,
    account_id uuid NOT NULL,
    container_id int8 NOT NULL, -- FOREIGN KEY warehousing.containers::container_id
    location_id int8 NOT NULL, -- два диапазона [1 - 10^18-1] and [10^18 - inf]
    container_quantity int4 NOT NULL,
    container_volume double precision NOT NULL, -- meter3 (auto calculated)
    created_at timestamp with time zone NOT NULL DEFAULT NOW()
);

CREATE UNIQUE INDEX uk_containers_container_id_location_id ON warehousing.container_stocks (container_id, location_id);
CREATE INDEX ik_containers_account_id ON warehousing.container_stocks (account_id);
CREATE INDEX ik_containers_location_id ON warehousing.container_stocks (location_id);

-- --------------------------------------------------------------------------------------------------

CREATE TABLE warehousing.container_stocks_log (
