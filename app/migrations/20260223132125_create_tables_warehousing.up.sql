    deleted_at timestamp with time zone NULL DEFAULT NOW()
);

CREATE INDEX ik_container_stocks_log_account_id ON warehousing.container_stocks_log (account_id);
CREATE INDEX ik_container_stocks_log_container_id ON warehousing.container_stocks_log (container_id);
CREATE INDEX ik_container_stocks_log_location_id ON warehousing.container_stocks_log (location_id);
CREATE INDEX ik_container_stocks_log_created_at ON warehousing.container_stocks_log (created_at);
