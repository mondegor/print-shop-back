    updated_at timestamp with time zone NOT NULL DEFAULT NOW(),
    deleted_at timestamp with time zone NULL
);

CREATE UNIQUE INDEX uk_stores_territory_id_store_code ON warehousing.stores (territory_id, store_code) WHERE deleted_at IS NULL;
CREATE INDEX ik_stores_account_id ON warehousing.stores (account_id) WHERE deleted_at IS NULL;

