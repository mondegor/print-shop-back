-- история контейнеров
CREATE TABLE warehousing.container_history (
    container_id int8 NOT NULL CONSTRAINT pk_container_history PRIMARY KEY,
    account_id uuid NOT NULL,
    container_code character varying(16) NOT NULL,
    container_marker int2 NOT NULL,
    container_tags jsonb NOT NULL, -- list of tags
    container_images jsonb NOT NULL, -- list of images
    container_volume jsonb NOT NULL, -- width, length, height
    created_at timestamp with time zone NOT NULL,
    updated_at timestamp with time zone NOT NULL,
    deleted_at timestamp with time zone NULL DEFAULT NOW()
);

CREATE INDEX ik_container_history_account_id_container_code ON warehousing.container_history (account_id, container_code);

