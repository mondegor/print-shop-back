CREATE TABLE warehousing.containers (
    container_id int8 NOT NULL CONSTRAINT pk_containers PRIMARY KEY, -- location_id = [10^18 - inf)
    tag_version int4 NOT NULL DEFAULT 1 CHECK(tag_version > 0),
    account_id uuid NOT NULL,
    container_code character varying(32) NOT NULL, -- prefix: "G/" - group, "O/" - заказ, "M/" - материал, "A/" - любой предмет
    container_marker int2 NOT NULL, -- auto generated
    container_volume jsonb NOT NULL, -- width, length, height (if group then recalculated)
    container_tags jsonb NULL, -- list of tags
    container_images jsonb NULL, -- list of images
    created_at timestamp with time zone NOT NULL DEFAULT NOW(),
    updated_at timestamp with time zone NOT NULL DEFAULT NOW()
);

CREATE UNIQUE INDEX uk_containers_account_id_code_marker ON warehousing.containers (account_id, container_code, container_marker);
CREATE INDEX ik_containers_account_id_container_tags ON warehousing.containers (account_id, container_tags);

-- sequence name = table_name + "_" + primary_key_name + "_seq"
CREATE SEQUENCE warehousing.containers_group_id_seq START 1200000000000000001;
CREATE SEQUENCE warehousing.containers_container_id_seq START 2400000000000000001;

